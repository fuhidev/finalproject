package tdt.it.finalproject.jsondata;

public abstract class  AssetPrice {
	private int id;
	private String name;
	public AssetPrice(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
